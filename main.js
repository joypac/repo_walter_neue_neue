// Infinite scroll: append a new randomised grid when sentinel comes into view
var sentinel = document.getElementById('scroll-sentinel');
var appending = false;

function appendGrid() {
  if (appending) return;
  appending = true;
  var grid = document.createElement('div');
  grid.className = 'mosaic-grid';
  sentinel.parentNode.insertBefore(grid, sentinel);
  buildMosaicLayout(grid);
  appending = false;
  gridRecycler.observe(grid);
}

/* ── Grid recycler ───────────────────────────────────────
   Removes children from grids that scroll far off-screen
   (beyond 3× viewport height). Preserves height with a
   placeholder so scroll position stays stable. Restores
   content when the grid comes back into range.
── */
var gridRecycler = (function() {
  var savedGrids = new WeakMap();

  // Margin: keep grids alive within 3× viewport above and below.
  var obs = new IntersectionObserver(function(entries) {
    entries.forEach(function(entry) {
      var grid = entry.target;
      if (entry.isIntersecting) {
        // Restore if previously recycled
        var saved = savedGrids.get(grid);
        if (!saved) return;
        savedGrids.delete(grid);
        grid.style.minHeight = '';
        // Rebuild content into this grid
        buildMosaicLayout(grid);
      } else {
        // Already recycled — skip
        if (savedGrids.has(grid)) return;
        // Preserve height, remove children
        var h = grid.getBoundingClientRect().height;
        savedGrids.set(grid, true);
        // Unobserve videos in this grid
        var vids = grid.querySelectorAll('.is-video');
        for (var i = 0; i < vids.length; i++) vidObs.unobserve(vids[i]);
        grid.innerHTML = '';
        grid.style.minHeight = h + 'px';
      }
    });
  }, {
    rootMargin: (window.innerHeight * 3) + 'px 0px'
  });

  return {
    observe: function(grid) { obs.observe(grid); }
  };
})();

// Include the first grid in the recycler.
gridRecycler.observe(mosaicEl);

function dynamicRootMargin() {
  return Math.max(400, Math.round(window.innerHeight * 1.5)) + 'px';
}

var infiniteObs = null;
function setupInfiniteObserver() {
  if (infiniteObs) infiniteObs.disconnect();
  infiniteObs = new IntersectionObserver(function(entries) {
    if (!entries[0].isIntersecting) return;
    appendGrid();
  }, { rootMargin: dynamicRootMargin() });
  infiniteObs.observe(sentinel);
}

setupInfiniteObserver();

// Keeps enough content ahead so frame jumps do not hit the page end.
function ensureContentAhead(minAheadPx) {
  var maxScroll = Math.max(0, document.documentElement.scrollHeight - window.innerHeight);
  var remaining = maxScroll - window.scrollY;
  var attempts = 0;

  while (remaining < minAheadPx && attempts < 3) {
    appendGrid();
    maxScroll = Math.max(0, document.documentElement.scrollHeight - window.innerHeight);
    remaining = maxScroll - window.scrollY;
    attempts++;
  }
}

var ContactOverlay = (function() {
  var overlay = document.getElementById('contact-overlay');
  var panel = document.getElementById('contact-panel');
  var trigger = document.getElementById('contact-trigger');
  var sections = Array.prototype.slice.call(panel.querySelectorAll('.contact-section'));
  var toggles = Array.prototype.slice.call(panel.querySelectorAll('.contact-section-toggle'));
  var open = false;

  function setOpenSection(nextIndex) {
    sections.forEach(function(section, idx) {
      var isOpenSection = nextIndex > -1 && idx === nextIndex;
      section.classList.toggle('is-open', isOpenSection);
    });

    toggles.forEach(function(toggle, idx) {
      toggle.setAttribute('aria-expanded', nextIndex > -1 && idx === nextIndex ? 'true' : 'false');
    });
  }

  function positionPanelBelowTrigger() {
    var rect = trigger.getBoundingClientRect();
    var triggerStyles = window.getComputedStyle(trigger);
    var textStartX = rect.left + parseFloat(triggerStyles.paddingLeft || '0');
    var left = Math.max(8, Math.round(textStartX));
    var top = Math.round(rect.bottom - 5);
    panel.style.left = left + 'px';
    panel.style.top = top + 'px';
  }

  function syncA11y() {
    overlay.setAttribute('aria-hidden', open ? 'false' : 'true');
    trigger.setAttribute('aria-expanded', open ? 'true' : 'false');
  }

  function openOverlay() {
    if (open) return;
    open = true;
    overlay.style.willChange = 'clip-path';
    overlay.classList.add('is-open');
    positionPanelBelowTrigger();
    setOpenSection(-1);
    syncA11y();
    setTimeout(function() {
      panel.focus();
    }, 0);
  }

  function closeOverlay() {
    if (!open) return;
    open = false;
    overlay.classList.remove('is-open');
    syncA11y();
    trigger.focus();
    // Remove will-change after transition ends to free GPU layer.
    overlay.addEventListener('transitionend', function cleanup() {
      overlay.removeEventListener('transitionend', cleanup);
      if (!open) overlay.style.willChange = '';
    });
  }

  function toggleOverlay() {
    if (open) closeOverlay();
    else openOverlay();
  }

  trigger.addEventListener('click', function(e) {
    e.stopPropagation();
    toggleOverlay();
  });

  overlay.addEventListener('click', function(e) {
    var rect = panel.getBoundingClientRect();
    var margin = 28;
    var insideSafeArea =
      e.clientX >= rect.left - margin &&
      e.clientX <= rect.right + margin &&
      e.clientY >= rect.top - margin &&
      e.clientY <= rect.bottom + margin;

    if (insideSafeArea) return;
    closeOverlay();
  });

  panel.addEventListener('click', function(e) {
    e.stopPropagation();
  });

  toggles.forEach(function(toggle, idx) {
    toggle.addEventListener('click', function(e) {
      e.stopPropagation();
      var section = sections[idx];
      var willOpen = !section.classList.contains('is-open');
      setOpenSection(willOpen ? idx : -1);
    });
  });

  window.addEventListener('resize', function() {
    if (!open) return;
    positionPanelBelowTrigger();
  }, { passive: true });

  window.addEventListener('orientationchange', function() {
    if (!open) return;
    positionPanelBelowTrigger();
  }, { passive: true });

  return {
    isOpen: function() { return open; },
    open: openOverlay,
    close: closeOverlay,
    toggle: toggleOverlay
  };
})();

