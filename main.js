function shuffle(arr) {
  var a = arr.slice();
  for (var i = a.length - 1; i > 0; i--) {
    var j = Math.floor(Math.random() * (i + 1));
    var t = a[i]; a[i] = a[j]; a[j] = t;
  }
  return a;
}

var DIRS = [[-1,0],[1,0],[0,-1],[0,1]];

// Retained references prevent GC from evicting preloaded images.
var imageCache = [];

function warmImageCache() {
  var seen = Object.create(null);
  var queue = [];

  projects.forEach(function(project) {
    project.media.forEach(function(item) {
      if (item.type !== 'img') return;
      if (seen[item.src]) return;
      seen[item.src] = true;
      queue.push(item.src);
    });
  });

  if (!queue.length) return;

  function consumeChunk(deadline) {
    var budget = deadline && typeof deadline.timeRemaining === 'function'
      ? Math.max(2, Math.floor(deadline.timeRemaining()))
      : 5;

    var count = 0;
    while (queue.length && count < budget) {
      var src = queue.shift();
      var img = new Image();
      img.decoding = 'async';
      img.loading = 'eager';
      img.src = src;
      imageCache.push(img);
      count++;
    }

    if (!queue.length) return;

    if ('requestIdleCallback' in window) {
      window.requestIdleCallback(consumeChunk, { timeout: 800 });
    } else {
      setTimeout(function() {
        consumeChunk();
      }, 80);
    }
  }

  if ('requestIdleCallback' in window) {
    window.requestIdleCallback(consumeChunk, { timeout: 1200 });
  } else {
    setTimeout(function() {
      consumeChunk();
    }, 220);
  }
}

/* ── Compact grid builder ─────────────────────────────────
   Fills a COLS×ROWS grid completely with project indices.
   No empty cells. Uses multi-source BFS with an anti-snake
   heuristic: prefer expanding into cells that already have
   2+ project neighbours (makes blobs, not tendrils).
── */

function buildGrid(projects, displayedMedia, COLS) {
  var total = projects.reduce(function(s, p) { return s + p.media.length; }, 0);
  var ROWS  = Math.ceil(total / COLS);

  // grid[r][c] = project index, -1 = empty
  var grid = [];
  for (var r = 0; r < ROWS; r++) {
    var row = [];
    for (var c = 0; c < COLS; c++) row.push(-1);
    grid.push(row);
  }

  // Shuffle project order and plant spaced-out seeds
  var order   = shuffle(projects.map(function(_, i) { return i; }));
  var step    = Math.max(1, Math.floor(ROWS / projects.length));

  order.forEach(function(pi, oi) {
    var r = Math.min(oi * step, ROWS - 1);
    var c = Math.floor(Math.random() * COLS);
    for (var i = 0; i < ROWS * COLS; i++) {
      if (grid[r][c] === -1) { grid[r][c] = pi; return; }
      c = (c + 1) % COLS;
      if (c === 0) r = (r + 1) % ROWS;
    }
  });

  // Count placed seeds
  var counts = projects.map(function(_, pi) {
    var n = 0;
    for (var r = 0; r < ROWS; r++)
      for (var c = 0; c < COLS; c++)
        if (grid[r][c] === pi) n++;
    return n;
  });

  // Expand all projects round-robin until each hits its quota
  var growing = true;
  while (growing) {
    growing = false;
    order.forEach(function(pi) {
      if (counts[pi] >= projects[pi].media.length) return;

      // Build scored frontier (adjacent empty cells)
      var frontier = [], seen = {};
      for (var r = 0; r < ROWS; r++) {
        for (var c = 0; c < COLS; c++) {
          if (grid[r][c] !== pi) continue;
          for (var di = 0; di < DIRS.length; di++) {
            var nr = r + DIRS[di][0], nc = c + DIRS[di][1];
            var key = nr + ',' + nc;
            if (nr < 0 || nr >= ROWS || nc < 0 || nc >= COLS) continue;
            if (grid[nr][nc] !== -1 || seen[key]) continue;
            seen[key] = true;
            // Score = number of existing pi-neighbours (anti-snake: prefer pockets)
            var score = 0;
            for (var dj = 0; dj < DIRS.length; dj++) {
              var ar = nr + DIRS[dj][0], ac = nc + DIRS[dj][1];
              if (ar >= 0 && ar < ROWS && ac >= 0 && ac < COLS && grid[ar][ac] === pi) score++;
            }
            frontier.push([nr, nc, score]);
          }
        }
      }

      var pick;
      if (frontier.length > 0) {
        // Pick from cells with the highest score (most neighbours = blobby, not snake)
        frontier.sort(function(a, b) { return b[2] - a[2]; });
        var best = frontier[0][2];
        var top  = [];
        for (var i = 0; i < frontier.length && frontier[i][2] === best; i++) top.push(frontier[i]);
        pick = top[Math.floor(Math.random() * top.length)];
      } else {
        // Completely surrounded — pick any remaining empty cell (fallback)
        var any = [];
        for (var r = 0; r < ROWS; r++)
          for (var c = 0; c < COLS; c++)
            if (grid[r][c] === -1) any.push([r, c, 0]);
        if (any.length === 0) return;
        pick = any[Math.floor(Math.random() * any.length)];
      }

      grid[pick[0]][pick[1]] = pi;
      counts[pi]++;
      growing = true;
    });
  }

  // Flood-fill any remaining empty cells with the nearest project
  // (at most COLS-1 cells can remain unfilled if total % COLS !== 0)
  var changed = true;
  while (changed) {
    changed = false;
    for (var r = 0; r < ROWS; r++) {
      for (var c = 0; c < COLS; c++) {
        if (grid[r][c] !== -1) continue;
        for (var di = 0; di < DIRS.length; di++) {
          var nr = r + DIRS[di][0], nc = c + DIRS[di][1];
          if (nr >= 0 && nr < ROWS && nc >= 0 && nc < COLS && grid[nr][nc] !== -1) {
            grid[r][c] = grid[nr][nc];
            changed = true;
            break;
          }
        }
      }
    }
  }

  return { grid: grid, rows: ROWS, cols: COLS };
}

